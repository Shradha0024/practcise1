package c_MethodWithReturnType;

public class Sample2 //method with string return type and method overloading
{
	
	public String convetToUpperCase(String name) 
	{
		String c1 = name.toUpperCase();
		return c1;
	}
	
	public int convetToUpperCase(int j, int k) 
	{
		int c2 = j+k;
		return c2;
	}
	
}
